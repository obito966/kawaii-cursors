// ─── RENDER GRID ──────────────────────────────────────────────────────────────
function renderGrid() {
  const grid = $('cursorGrid');
  const empty = $('emptyState');

  // Merge default + uploaded cursors
  const combined = [...allCursors, ...uploadedCursors];

  const filtered = combined.filter(c => {
    const catMatch = activeFilter === 'all' || c.cat === activeFilter;
    const searchMatch = !searchQuery || c.name.toLowerCase().includes(searchQuery);
    return catMatch && searchMatch;
  });

  if (filtered.length === 0) {
    grid.innerHTML = '';
    empty.hidden = false;
    return;
  }
  empty.hidden = true;
  grid.innerHTML = filtered.map((c, i) => buildCard(c, i)).join('');

  // Attach card events
  grid.querySelectorAll('[data-preview]').forEach(btn => {
    btn.addEventListener('click', () => openModal(parseInt(btn.dataset.preview)));
  });
  grid.querySelectorAll('[data-download]').forEach(btn => {
    btn.addEventListener('click', () => downloadCursor(parseInt(btn.dataset.download)));
  });

  updateStats(combined.length);
}

function buildCard(c, index) {
  const delay = (index % 12) * 50;
  const imageHtml = c.type === 'uploaded'
    ? `<img class="card-image" src="${API_BASE}/uploads/${c.filename}" alt="${c.name}" loading="lazy" />`
    : `<div class="card-emoji">${c.emoji}</div>`;

  const uploadedTag = c.type === 'uploaded' ? `<span class="card-uploaded-tag">USER</span>` : '';

  return `
    <div class="cursor-card" style="animation-delay:${delay}ms">
      ${uploadedTag}
      <div class="card-image-wrap">
        ${imageHtml}
      </div>
      <div class="card-body">
        <h3 class="card-title">${c.name}</h3>
        <div class="card-meta">
          <span class="cat-badge ${c.cat}">${catLabel(c.cat)}</span>
        </div>
        <div class="card-actions">
          <button class="card-btn card-btn-preview" data-preview="${c.id}">👁 Preview</button>
          <button class="card-btn card-btn-download" data-download="${c.id}">⬇ Save</button>
        </div>
      </div>
    </div>`;
}

